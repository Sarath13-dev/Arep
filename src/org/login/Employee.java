package org.login;

//hierarchial Inheritence
//Parent class
public class Employee {
	
	public void empId() {
		System.out.println("employee id is 1234");
	}
	
	public void empName() {
		System.out.println("employee name is ravi");
	}	
	
	public static void main(String[] args) {
		Employee a = new Employee();
		a.empId();
		a.empName();
		
	}
	
}















